from collections import defaultdict

def load_dictionary(path):
    dictionary = defaultdict(list)
    with open(path, "r") as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) >= 2:
                word = parts[0]
                phonemes = parts[1:]
                dictionary[word].append(phonemes)
    return dictionary

