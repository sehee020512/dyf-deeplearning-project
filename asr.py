def sort_recognized_file(input_path="recognized.txt", output_path="recognized.txt"):
    with open(input_path, "r", encoding="utf-8") as f:
        lines = f.read().strip().split("\n")

    blocks = []
    block = []

    for line in lines:
        line = line.strip()
        if line.startswith("\"") and block:
            blocks.append(block)
            block = [line.replace(".lab", ".rec")]  # í™•ìž¥ìž ë³€ê²½
        else:
            block.append(line)
    if block:
        blocks.append(block)

    # í—¤ë” ê¸°ì¤€ ì •ë ¬
    blocks.sort(key=lambda x: x[0])  # "mfc/..." ê¸°ì¤€

    with open(output_path, "w", encoding="utf-8") as f:
        f.write("#!MLF!#\n")

        for block in blocks:
            for idx, line in enumerate(block):
                if idx == 0:
                    f.write(line + "\n")
                else:
                    cleaned = line.replace("<s>", "").strip()
                    if cleaned:
                        f.write(cleaned + "\n")

