def parse_bigram_file(file_path):
    bigram_probs = defaultdict(dict)

    with open(file_path, "r") as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) != 3:
                continue  # ìž˜ëª»ëœ ë¼ì¸ ë¬´ì‹œ
            prev, next_, prob = parts
            bigram_probs[prev][next_] = float(prob)

    return bigram_probs

